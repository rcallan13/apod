extension UIColor {
    
    static func titleGray() -> UIColor {
        return UIColor(red: 0x7A, green: 0x7A, blue: 0x7A, alpha: 1)
    }
    
    static func shadowGray() -> UIColor {
        return UIColor(red: 0xFA, green: 0xFA, blue: 0xFA, alpha: 0.4)
    }
}
